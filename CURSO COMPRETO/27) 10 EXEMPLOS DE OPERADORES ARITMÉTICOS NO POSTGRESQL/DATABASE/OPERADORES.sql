-- Realiza uma adição simples de dois números.
SELECT 10 + 12 AS Resultado;

-- Realiza uma subtração de dois números.
SELECT -10 + 12 AS Resultado;

-- Realiza uma operação de multiplicação e, em seguida, uma adição.
SELECT 10 + 6 * 3 AS Resultado;

-- Utiliza parênteses para alterar a ordem de precedência da operação.
SELECT (10 + 6) * 3 AS Resultado;

-- Calcula o resto da divisão de 18 por 2.
SELECT 18 % 2 AS Resultado;

-- Calcula o resto da divisão de 18 por 4.
SELECT 18 % 4 AS Resultado;

-- Realiza uma operação de potenciação.
SELECT 10 ^ 2 AS Resultado;

-- Utiliza notação científica para representar um número em ponto flutuante.
SELECT 6.02 * 10 ^ 23 AS Resultado;

-- Realiza uma operação envolvendo notação científica.
SELECT 6.02e+23 * 5 AS Resultado;

-- Calcula a raiz quadrada de 144.
SELECT |/ 144 AS Resultado;

-- Calcula a raiz quadrada de 170.
SELECT |/ 170 AS Resultado;

-- Calcula a raiz quadrada cúbica de 170.
SELECT ||/ 170 AS Resultado;

-- Calcula a raiz quadrada cúbica de 27.
SELECT ||/ 27 AS Resultado;
